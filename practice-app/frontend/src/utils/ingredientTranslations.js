/**
 * Translate ingredient name based on language
 * @param {string} ingredientName - Original ingredient name (English)
 * @param {string} language - Target language (en or tr)
 * @returns {string} Translated ingredient name
 */
export const translateIngredient = (ingredientName, language = 'en') => {
  if (!ingredientName) return ingredientName;
  
  // Try exact match first
  if (ingredientTranslations[ingredientName]) {
    return ingredientTranslations[ingredientName][language] || ingredientName;
  }
  
  // Try case-insensitive match
  const normalizedName = ingredientName.trim();
  const translationKey = Object.keys(ingredientTranslations).find(
    key => key.toLowerCase() === normalizedName.toLowerCase()
  );
  
  if (translationKey) {
    return ingredientTranslations[translationKey][language] || ingredientName;
  }
  
  // If no translation, return original name
  return ingredientName;
};

/**
 * Get ingredient translation object
 * @param {string} ingredientName - Ingredient name
 * @returns {object} Translation object {en: string, tr: string}
 */
export const getIngredientTranslation = (ingredientName) => {
  return ingredientTranslations[ingredientName] || { en: ingredientName, tr: ingredientName };
};

